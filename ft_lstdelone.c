#include "libft.h"
#include <stdio.h>
#include <stdlib.h>

/* 
	esta función consiste en liberar el nodo lst mediante una función aux del
*/

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
